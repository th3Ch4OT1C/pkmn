var mouseTimeout;

function random(min, max) {
    return Math.floor(Math.random() * ((max + 1) - min)) + min;
}

function genPkmn() {
    $.each($("img"), function() {
        if ($(this).attr('data-hasPkmn') === true) {
            $(this).attr('src', $(this).attr('data-oldSrc'));
            $(this).off('click');
        }
        if ($(this).width() < 40 && $(this).height() < 40) {
            if (Math.random() > 0.9) {
                let pkmnRarity = random(1, 10);
                if (random(1, 10) >= pkmnRarity) {
                    let pkmn = rarity[pkmnRarity][random(0, rarity[pkmnRarity].length - 1)];
                    pkmn = pkmn.toLowerCase();
                    $(this).attr('data-pkmn', pkmn);
                    $(this).attr('data-hasPkmn', true);
                    $(this).attr('data-oldSrc', $(this).attr('src'));
                    $(this).attr('src', 'https://img.pokemondb.net/sprites/sun-moon/icon/' + pkmn + '.png');
                    $(this).click(function() {
                        event.preventDefault();
                        let pkmn = $(this).attr('data-pkmn');
                        if (!window.localStorage.getItem("obtainedPkmn")) {
                            window.localStorage.setItem("obtainedPkmn", "[]");
                        }
                        console.log(window.localStorage.getItem("obtainedPkmn"));
                        if (JSON.parse(window.localStorage.getItem("obtainedPkmn")).indexOf(pkmn) === -1) {
                            let list = JSON.parse(window.localStorage.getItem("obtainedPkmn"));
                            list.push(pkmn);
                            window.localStorage.setItem("obtainedPkmn", JSON.stringify(list));
                        }
                        $(this).attr('src', $(this).attr('data-oldSrc'));
                        $(this).off('click');
                    });
                }
            }
        }
    });
}

function spawnPkmn() {
    genPkmn();
    setTimeout(spawnPkmn, random(3, 12) * 10000);
}

$(document).ready(function() {
    $(".hover-display").hide();
    $(".pl-controls-top, .pl-controls-bottom").css('background', 'none');
    setTimeout(genPkmn, 5000);
    setTimeout(spawnPkmn, random(3, 12) * 10000);
});

document.onmousemove = function(event) {
    $(document.body).css('cursor', 'default');
    $(".hover-display").show();
    clearTimeout(mouseTimeout);
    mouseTimeout = setTimeout(function() {$(document.body).css('cursor', 'none');$(".hover-display").hide();}, 500);
};
